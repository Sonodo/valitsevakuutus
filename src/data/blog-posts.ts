/**
 * Get related blog posts based on shared tags and insurance types.
 * Excludes the current post and returns most relevant matches.
 */
export function getRelatedBlogPosts(slug: string, limit: number = 3): BlogPost[] {
  const current = getBlogPostBySlug(slug);
  if (!current) return [];

  const scored = blogPosts
    .filter((p) => p.slug !== slug)
    .map((post) => {
      let score = 0;
      // Shared tags
      score += post.tags.filter((t) => current.tags.includes(t)).length * 2;
      // Same category
      if (post.category === current.category) score += 3;
      // Shared insurance types
      if (post.relatedInsuranceTypes && current.relatedInsuranceTypes) {
        score += post.relatedInsuranceTypes.filter((t) =>
          current.relatedInsuranceTypes!.includes(t)
        ).length * 2;
      }
      return { post, score };
    })
    .sort((a, b) => b.score - a.score);

  return scored.slice(0, limit).map((s) => s.post);
}

/**
 * Get the latest blog posts sorted by publish date.
 */
